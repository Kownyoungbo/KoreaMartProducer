package com.koreajt.producer.util;

public class ThumbImageInfo
{
  private String id;
  private String data;
  private String imageIdCol;
  private String sizeCol;
  
  public String getId()
  {
    return id;
  }
  public void setId(String id)
  {
    this.id = id;
  }
  public String getData()
  {
    return data;
  }
  public void setData(String data)
  {
    this.data = data;
  }
  public String getImageIdCol()
  {
    return imageIdCol;
  }
  public void setImageIdCol(String imageIdCol)
  {
    this.imageIdCol = imageIdCol;
  }
  public String getSizeCol()
  {
    return sizeCol;
  }
  public void setSizeCol(String sizeCol)
  {
    this.sizeCol = sizeCol;
  }
}
